package com.pharma.model.parent;

public class PresentAbsentModel {
    private String month;
    private int present;
    private int absent;
    private int year1;
    private int mont1;
    private byte total;

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }

    public int getPresent() {
        return present;
    }

    public void setPresent(int present) {
        this.present = present;
    }

    public int getAbsent() {
        return absent;
    }

    public void setAbsent(int absent) {
        this.absent = absent;
    }


    public int getYear1() {
        return year1;
    }

    public void setYear1(int year1) {
        this.year1 = year1;
    }

    public int getMont1() {
        return mont1;
    }

    public void setMont1(int mont1) {
        this.mont1 = mont1;
    }

    public byte getTotal() {
        return total;
    }

    public void setTotal(byte total) {
        this.total = total;
    }
}
